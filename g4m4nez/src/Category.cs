public class Category
{
    private string icon;
    public string Icon
    {
        get { return icon; }
        set { icon = value; }
    }

    private string name;
    public string Name
    {
        get { return name; }
        set { name = value; }
    }

    private string description;
    public string Description
    {
        get { return description; }
        set { description = value; }
    }
}